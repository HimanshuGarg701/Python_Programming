# press anything from o,a,b,c,f and print different thins according to it.
x = input("enter any of o,a,b,c,f: ")
if x is 'o':
    print("Outstanding")
elif x is 'a':
    print("very good")
elif x is 'b':
    print("good")
elif x is "c":
    print("average")
elif x is "f":
    print("fail")
else:
    print("wrong input")
