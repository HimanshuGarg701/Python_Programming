# ======================================CUBES===============================================================
# pattern of cubes
for i in range(1,11):
    print(i**3,end=" ")
print("\n")


# =======================================add by 3===========================================================
# a unique series of AP with common diff 3
for a in range(-5,23,3):
    print(a,end=" ")
print("\n")


# =======================================add by 2===========================================================
# a unique series of AP with common diff 2
for b in range(-2,-21,-2):
    print(b,end=" ")
print("\n")


#========================================add by 3(positives)================================================
# a unique series of AP with common diff 3
for c in range(1,32,3):
    print(c,end=" ")
